def github_api(url, method="GET", data=None):
    """Make a GitHub API request."""
    try:
        headers = {
            "Authorization": f"token {GITHUB_TOKEN}",
            "Accept": "application/vnd.github.v3+json",
        }
        if data is not None:
            req = urllib.request.Request(url, data=json.dumps(data).encode(), headers=headers)
            req.get_method = lambda: method
        else:
            req = urllib.request.Request(url, headers=headers)
        
        with urllib.request.urlopen(req, timeout=30) as resp:
            return json.loads(resp.read())
    except urllib.error.HTTPError as e:
        return {"error": f"HTTP {e.code}", "message": e.read().decode()[:200]}
    except Exception as e:
        return {"error": str(e)}

# ============ 1. CREATE ISSUES ============
def create_issue(repo, title, body, labels=None):
    """Create a GitHub issue for a bug found by NEXUS."""
    data = {"title": title, "body": body}
    if labels:
        data["labels"] = labels
    return github_api(f"https://api.github.com/repos/{repo}/issues", method="POST", data=data)

def nexus_analysis_to_issues(repo, analysis_result, file_path):
    """Convert NEXUS analysis results into GitHub Issues."""
    issues_created = []
    issues = analysis_result.get("issues", [])
    session_id = analysis_result.get("session_id", "")
    verdict = analysis_result.get("verdict", {})
    
    for issue in issues:
        if not isinstance(issue, dict):
            continue
        
        severity = str(issue.get("severity", "unknown")).lower()
        issue_type = issue.get("type", "bug")
        description = issue.get("description", "No description")
        line = issue.get("line", "?")
        
        # Only create issues for critical/high severity
        if severity not in ("critical", "high"):
            continue
        
        title = f"[NEXUS] [{severity.upper()}] {issue_type}: {description[:60]}"
        body = f"""## NEXUS Swarm Analysis

**File:** `{file_path}` (Line {line})
**Severity:** {severity.upper()}
**Type:** {issue_type}
**Session:** {session_id}

### Description
{description}

### Analysis
- **Issues found by SCOUT:** {len(issues)}
- **JUDGE verdict:** {verdict.get('verdict', '?')} ({verdict.get('score', 0)}/100)
- **Scanner:** NEXUS 7-agent swarm + Mamba-3 SSM GPU
- **Verification:** Lean4 + Conscience anti-hallucination

### Fix
A fix has been generated by FORGE. See the PR description for details.

---
_Automatically created by NEXUS-AUTONOMOS_
"""
        
        labels = [f"nexus-{severity}", issue_type, "automated"]
        result = create_issue(repo, title, body, labels)
        
        if "html_url" in result:
            issues_created.append(result["html_url"])
            sys.stderr.write(f"[GITHUB] Issue created: {result['html_url']}\n")
        else:
            sys.stderr.write(f"[GITHUB] Issue creation failed: {result.get('error','?')}\n")
        
        time.sleep(1)  # Rate limit
    
    return issues_created

