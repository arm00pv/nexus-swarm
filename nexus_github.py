def create_github_actions(repo):
    """Create a GitHub Actions workflow file in the repo."""
    # We need to create the file via the API
    # First check if it exists
    existing = github_api(f"https://api.github.com/repos/{repo}/contents/.github/workflows/nexus-analysis.yml")
    
    if "sha" in existing:
        # File exists, update it
        sha = existing["sha"]
    else:
        sha = None
    
    import base64
    content = base64.b64encode(GITHUB_ACTIONS_WORKFLOW.encode()).decode()
    data = {
        "message": "NEXUS: Add automated code analysis workflow",
        "content": content,
    }
    if sha:
        data["sha"] = sha
    
    return github_api(
        f"https://api.github.com/repos/{repo}/contents/.github/workflows/nexus-analysis.yml",
        method="PUT",
        data=data
    )

# ============ 3. REPO METADATA (Topics + Description) ============
def update_repo_metadata(repo, description, topics, homepage=None):
    """Update repo description and topics for SEO."""
    # Update description
    data = {"description": description}
    if homepage:
        data["homepage"] = homepage
    github_api(f"https://api.github.com/repos/{repo}", method="PATCH", data=data)
    
    # Update topics
    github_api(
        f"https://api.github.com/repos/{repo}/topics",
        method="PUT",
        data={"names": topics}
    )

# ============ 4. GITHUB RELEASES ============
def create_release(repo, tag, name, body, draft=False):
    """Create a GitHub Release."""
    return github_api(
        f"https://api.github.com/repos/{repo}/releases",
        method="POST",
        data={
            "tag_name": tag,
            "name": name,
            "body": body,
            "draft": draft,
        }
    )

# ============ 5. WEBHOOK SETUP ============
def create_webhook(repo, url, events=None):
    """Create a webhook to auto-trigger NEXUS on push events."""
    if events is None:
        events = ["push", "pull_request"]
    return github_api(
        f"https://api.github.com/repos/{repo}/hooks",
        method="POST",
        data={
            "name": "web",
            "config": {"url": url, "content_type": "json"},
            "events": events,
            "active": True,
        }
    )

